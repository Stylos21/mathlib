def add(a, b):
    return str(a + b)

def subtract(a, b):
    return str(a - b)

def multiply(a, b):
    return str(a * b)

def divide(a, b):
    return str(a / b)

def square_area(length, width):
    return str(length * width)

def circle_area(radius):
    return str((3.14 * radius^2))

def triangle_area(length, base):
    return str((length * base / 2))

def rectangle_perimeter(length, width):
    return str(2(length + width))


def square_perimeter(side):
    return str(4 * side)


def exponents(number,exponent):
    return str(number^exponent)
